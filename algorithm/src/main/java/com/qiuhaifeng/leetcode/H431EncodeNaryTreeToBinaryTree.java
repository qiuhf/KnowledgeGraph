package com.qiuhaifeng.leetcode;

import java.util.List;

/**
 * <pre>
 *      Encode N-ary Tree to Binary Tree
 * </pre>
 *
 * @author sz_qiuhf@163.com
 * @since 2021-07-13
 **/
public class H431EncodeNaryTreeToBinaryTree {

    class Node {
        public int val;
        public List<Node> children;

        public Node() {
        }

        public Node(int val) {
            this.val = val;
        }

        public Node(int val, List<Node> children) {
            this.val = val;
            this.children = children;
        }
    }

    class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode(int x) {
            val = x;
        }
    }

}
